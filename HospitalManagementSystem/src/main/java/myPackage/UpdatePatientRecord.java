    private void btnSearchIDActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSearchIDActionPerformed
    {//GEN-HEADEREND:event_btnSearchIDActionPerformed
        // TODO add your handling code here:
        String patientID = txtPatientID.getText();
        String query = "select * from patient where patientID = '"+patientID+"'";
         
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospital_database","root","mySQL");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            
            if(rs.first())
            {
                txtPatientID.setEditable(false);
                
                String name = rs.getString("name");
                String contactNumber = rs.getString("contactNumber");
                String age = rs.getString("age");
                String gender = rs.getString("gender");
                String bloodGroup = rs.getString("bloodGroup");
                String address = rs.getString("address");
                String anyDisease = rs.getString("anyMajorDisease");
                
                txtName.setText(name);
                txtContact.setText(contactNumber);
                txtAge.setText(age);
                cmbGender.setSelectedItem(gender);
                txtBloodGroup.setText(bloodGroup);
                txtAddress.setText(address);
                txtPreviousDisease.setText(anyDisease);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Patient ID not found");
            }
            
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, "Connection error");
        }
    }//GEN-LAST:event_btnSearchIDActionPerformed

