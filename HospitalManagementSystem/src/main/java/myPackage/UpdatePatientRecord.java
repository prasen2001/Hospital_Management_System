    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnUpdateActionPerformed
    {//GEN-HEADEREND:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        String patientID = txtPatientID.getText();
        String name = txtName.getText();
        String contactNumber = txtContact.getText();
        String gender = (String) cmbGender.getSelectedItem();
        String age = txtAge.getText();
        String bloodGroup = txtBloodGroup.getText();
        String address = txtAddress.getText();
        String anyDisease = txtPreviousDisease.getText();

        String query = "update patient set name='"+name+"',contactNumber='"+contactNumber+"',age="+age+",gender='"+gender+"',bloodGroup='"+bloodGroup+"',address='"+address+"',anyMajorDisease='"+anyDisease+"' where patientID='"+patientID+"'";

        try
        {

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospital_database","root","mySQL");
            Statement st = con.createStatement();
            st.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Patient record saved successfully");

            txtPatientID.setText(null);
            txtName.setText(null);
            txtContact.setText(null);
            txtAge.setText(null);
            txtBloodGroup.setText(null);
            txtAddress.setText(null);
            txtPreviousDisease.setText(null);
            txtPatientID.setEditable(true);
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, "Error in insertion");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

